export function buildUserPrompt(passage: string, textbook?: string, passageNumber?: string): string {
  let prefix = '';
  if (textbook && passageNumber) {
    prefix = `교재명: ${textbook}, 지문번호: ${passageNumber}\n모든 소제목(인용문)에 반드시 "${textbook} ${passageNumber}"을 포함해줘.\n\n`;
  } else if (textbook) {
    prefix = `교재명: ${textbook}\n\n`;
  } else if (passageNumber) {
    prefix = `지문번호: ${passageNumber}\n\n`;
  }
  return `${prefix}다음 영어 지문을 위 규칙에 맞춰 분석해 줘:\n\n${passage}`;
}
